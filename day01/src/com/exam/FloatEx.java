package com.exam;
	

	// 실수형 : float, double (실수형의 기본 : double)
	// 			 float을 표기할 때는 마지막에 f를 붙여야 float로 인식한다. 
	




public class FloatEx {

	
	public static void main(String[] args) {
		float var1;
		double var2;
		var1 = 3.4f;
		var2 = 55.55;  // f안붙이면 더블로인식함 
		
				System.out.println(var1);
				System.out.println(var2);
		
	}
	
	
}
